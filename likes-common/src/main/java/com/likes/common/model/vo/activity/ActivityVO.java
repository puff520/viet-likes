package com.likes.common.model.vo.activity;

public class ActivityVO {
    private Integer id;
    private String title;

    public ActivityVO() {
    }

    public Integer getId() {
        return this.id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return this.title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
